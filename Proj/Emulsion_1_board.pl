% BOARD %

% generate NxN initial board
genInitBoard(GameState, N) :-
  genInitCol(GameState, N, 1).

genInitCol([], N, N).
genInitCol([Line|Tab], N, CurrN) :- 
  CurrN < N,
  C is mod(CurrN, 2),
  LineN is N + C - 1,
  genInitLine(Line, LineN, C),
  NewN is CurrN + 1,
  genInitCol(Tab, N, NewN).

genInitLine([], N, N).
genInitLine([C|L], N, CurrN) :-
  CurrN < N,
  C is mod(CurrN, 2),
  NewN is CurrN + 1,
  genInitLine(L, N, NewN).

% Get 7x7 initial board (checkered)
initial(GameState) :-
  genInitBoard(GameState, 8). % N is 8 - 1 = 7

